				LuaAPI.lua_pushstring(L, type);
				LuaAPI.lua_pushstring(L, id);
				LuaAPI.lua_pushstring(L, func);
				if (args != null)  { for (int __gen_i = 0; __gen_i < args.Length; ++__gen_i) translator.PushAny(L, args[__gen_i]); };
				
